function isActive(pathname: string, href: string, exact?: boolean) {
  if (exact) return pathname === href;
  return pathname === href || pathname.startsWith(href + "/");
}

export default function WhatsAppLayout({ children }: { children: React.ReactNode }) {
  const { user } = useAuth();
  const pathname = usePathname();
  const hasWhatsApp = user?.tipo_plano === "pro" || user?.tipo_plano === "premium";

  if (!hasWhatsApp) {
    return <div className="flex-1">{children}</div>;
  }

  return (
    <div className="flex flex-col h-[calc(100vh-4rem)]">
      {/* Sub-navigation */}
      <div className="bg-white border-b border-slate-100 shrink-0">
        <div className="flex items-center overflow-x-auto px-2 gap-0.5 h-12 scrollbar-none">
          {sections.map((s) => {
            const active = isActive(pathname, s.href, s.exact);
            return (
              <Link
                key={s.href}
                href={s.href}
                className={`flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs font-semibold whitespace-nowrap transition-all shrink-0 ${
                  active
                    ? "bg-emerald-50 text-emerald-700"
                    : "text-slate-500 hover:bg-slate-50 hover:text-slate-700"
                }`}
              >
                <span className={active ? "text-emerald-600" : "text-slate-400"}>{s.icon}</span>
                {s.label}
                {active && <span className="w-1 h-1 rounded-full bg-emerald-500 ml-0.5" />}
              </Link>
            );
          })}
        </div>
      </div>

      {/* Page content */}
      <div className="flex-1 overflow-hidden">
        {children}
      </div>
    </div>
  );
}
